    private void identifiant_fldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_identifiant_fldKeyTyped

        if (identifiant_fld.getText().trim().length() >= 10) {
            evt.consume();
        }

        char keyChar = evt.getKeyChar();
        if (Character.isLowerCase(keyChar)) {
            evt.setKeyChar(Character.toUpperCase(keyChar));
        }
    }//GEN-LAST:event_identifiant_fldKeyTyped

