    private void connecter_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connecter_btnActionPerformed

        JLabel message1 = new JLabel("Tous les champs sont obligatoires !");
        JLabel message2 = new JLabel("Identifiant ou mot de passe incorrect !");
        JLabel message3 = new JLabel("Connexion avec succès !");
        message1.setFont(new Font("Lucida Sans", Font.PLAIN, 14));
        message2.setFont(new Font("Lucida Sans", Font.PLAIN, 14));
        message3.setFont(new Font("Lucida Sans", Font.PLAIN, 14));

        if (identifiant_fld.getText().trim().isEmpty() || motdepasse_fld.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, message1, "e-Document", JOptionPane.WARNING_MESSAGE);
        } else {
            String loginQuery = "SELECT * FROM utilisateur WHERE identifiant = ? AND mot_de_passe = ?";

            try {
                MyConnection c = new MyConnection(false);
                c.MyExecPrepStat(loginQuery);
                c.pst.setString(1, identifiant_fld.getText());
                c.pst.setString(2, motdepasse_fld.getText());
                c.rs = c.pst.executeQuery();

                if (c.rs.next()) {
                    Menu m = new Menu();

                    String type = c.rs.getString("type");
                    if (!type.equals("Administrateur")) {
                        
                        m.jTabbedPane1.setEnabledAt(3, false);
                        m.supprimer_histo_btn.setEnabled(false);
                        
                    }else{
                        m.jTabbedPane1.setEnabledAt(3, true);
                        m.supprimer_histo_btn.setEnabled(true);
                    }
                    
                        JOptionPane.showMessageDialog(this, message3, "e-Document", JOptionPane.INFORMATION_MESSAGE);
                        m.menu_connected_user.setText(this.identifiant_fld.getText());
                        m.setVisible(true);
                        this.setVisible(false);

                } else {
                    JOptionPane.showMessageDialog(this, message2, "e-Document", JOptionPane.ERROR_MESSAGE);
                    identifiant_fld.setText("");
                    motdepasse_fld.setText("");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_connecter_btnActionPerformed

    private void identifiant_fldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_identifiant_fldKeyReleased

        if (!identifiant_fld.getText().trim().isEmpty()) {
            JLabel message = new JLabel("Identifiant doit être alphanumérique !");
            message.setFont(new Font("Lucida Sans", Font.PLAIN, 14));

            if (!identifiant_fld.getText().matches("^[a-zA-Z0-9]+$")) {
                JOptionPane.showMessageDialog(this, message, "e-Document", JOptionPane.WARNING_MESSAGE);
                identifiant_fld.setText("");
            }
        }
    }//GEN-LAST:event_identifiant_fldKeyReleased

